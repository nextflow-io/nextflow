package nextflow.cloud.google.batch.model

import groovy.transform.CompileStatic
import groovy.transform.ToString

/**
 * Define where the joib can be executed
 *
 * @author Paolo Di Tommaso <paolo.ditommaso@gmail.com>
 */
@CompileStatic
@ToString(includeNames = true, ignoreNulls = true, includePackage = false)
class LocationPolicy {

    // A list of allowed location names represented by internal URLs,
    // for example, zones/us-central1-a, regions/us-west1.
    // First location in the list should be a region.
    List<String> allowedLocations

    // A list of denied location names.
    List<String> deniedLocations

}
